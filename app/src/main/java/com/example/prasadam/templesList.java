package com.example.prasadam;

public class templesList {
    String templeName;
    String templeTime;
    String templeLocation;
    String templeDistance;
    int templeImage;

    public templesList(String templeName, String templeTime, String templeLocation, String templeDistance, int templeImage) {
        this.templeName = templeName;
        this.templeTime = templeTime;
        this.templeLocation = templeLocation;
        this.templeDistance = templeDistance;
        this.templeImage = templeImage;
    }
}
